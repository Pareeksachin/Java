
class sumDouble {
//printing sum
	public static void main(String[] args) {
		int n1 = Integer.parseInt(args[0]);
		int n2 = Integer.parseInt(args[1]);
		int sum = n1+n2;
		int sub = n1-n2;
		int mul = n1*n2;
		int div = n1/n2;
		
		System.out.println("Sum : " + sum);
		System.out.println("Sub : " + sub);
		System.out.println("mul : " + mul);
		System.out.println("div : " + div);
		

	}

}
